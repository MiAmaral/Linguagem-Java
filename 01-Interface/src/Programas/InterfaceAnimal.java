package Programas;

//Em Java, uma interface é uma coleção de métodos abstratos (métodos sem implementação)
//e constantes. Ela define um contrato que uma classe deve seguir, especificando os métodos 
//que a classe deve implementar.
//As interfaces são usadas para definir comportamentos que várias classes podem compartilhar.

public interface InterfaceAnimal {
	public void animalSom();
	public void animalAbrigo();

}
